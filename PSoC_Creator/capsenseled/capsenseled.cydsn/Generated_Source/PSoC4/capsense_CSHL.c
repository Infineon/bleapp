{

        uint32 i;
    uint32 state = 0u;
    
    for(i = 0u; i < capsense_TOTAL_WIDGET_COUNT; i++)
    {
        if (capsense_CheckIsWidgetActive(i) != 0u)
        {
            state = capsense_WIDGET_IS_ACTIVE;
        }
    }
    


    return state;
}


